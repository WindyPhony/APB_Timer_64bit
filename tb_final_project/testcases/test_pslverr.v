task run_test;
	begin
		$display("==============================================");
		$display("=========== TEST CASE: SLAVE ERROR ===========");
		$display("==============================================");

		sys_rst_n = 0;
		@(posedge sys_clk);
		sys_rst_n = 1;

		write(ADDR_TCR, 32'h0000_0001, 1'b0, 4'hf);
		write(ADDR_TCR, 32'h0000_0003, 1'b0, 4'hf);

		#1;
		if(pslverr == 1'b1) begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, PASS: div_en do not change when timer_en = 1, EXP: 1'b1, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");
		end	
		else begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, FAIL: div_en do not change when timer_en = 1, EXP: 1'b1, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");		        
		        cnt_fail = cnt_fail + 1;
		end

		read(ADDR_TCR);
	
		#1;
		if(rdata == 32'h0000_0001) begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, PASS: no enable write data, EXP: 32'h00000001, ACT: 32'h%0h", $time, rdata);
			$display("---------------------------------------------------------------------------------------");
		end	
		else begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, FAIL: no enable write data, EXP: 32'h00000001, ACT: 32'h%0h", $time, rdata);
			$display("---------------------------------------------------------------------------------------");		        
		        cnt_fail = cnt_fail + 1;
		end


    	
		write(ADDR_TCR, 32'h0000_0001, 1'b0, 4'h0);
		write(ADDR_TCR, 32'h0000_0003, 1'b0, 4'h0);

		#1;
		if(pslverr == 1'b0) begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, PASS: no error when pstrb[0] = 0, EXP: 1'b0, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");
		end	
		else begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, FAIL: no error when pstrb[0] = 0, EXP: 1'b0, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");		        
		        cnt_fail = cnt_fail + 1;
		end

		write(ADDR_TCR, 32'h0000_0000, 1'b0, 4'hf);
	
	//
		write(ADDR_TCR, 32'h0000_0503, 1'b0, 4'hf);
		write(ADDR_TCR, 32'h0000_0803, 1'b0, 4'hf);

		#1;
		if(pslverr == 1'b1) begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, PASS: div_val do not change when timer_en = 1, EXP: 1'b1, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");
		end	
		else begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, FAIL: div_val do not change when timer_en = 1, EXP: 1'b1, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");		        
		        cnt_fail = cnt_fail + 1;
		end
		
		read(ADDR_TCR);
	
		#1;
		if(rdata == 32'h0000_0503) begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, PASS: no enable write data, EXP: 32'h00000503, ACT: 32'h%0h", $time, rdata);
			$display("---------------------------------------------------------------------------------------");
		end	
		else begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, FAIL: no enable write data, EXP: 32'h00000503, ACT: 32'h%0h", $time, rdata);
			$display("---------------------------------------------------------------------------------------");		        
		        cnt_fail = cnt_fail + 1;
		end

		
		write(ADDR_TCR, 32'h0000_0503, 1'b0, 4'hf);
		write(ADDR_TCR, 32'h0000_0803, 1'b0, 4'h0);

		#1;
		if(pslverr == 1'b0) begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, PASS: no error when pstrb[0] = 0, EXP: 1'b0, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");
		end	
		else begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, FAIL: no error when pstrb[0] = 0, EXP: 1'b0, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");		        
		        cnt_fail = cnt_fail + 1;
		end

		write(ADDR_TCR, 32'h0000_0802, 1'b0, 4'hf);

	//
		write(ADDR_TCR, 32'h0000_0502, 1'b0, 4'hf);
		write(ADDR_TCR, 32'h0000_0a02, 1'b0, 4'hf);

		#1;
		if(pslverr == 1'b1) begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, PASS: div_val must be < 4'b1001, EXP: 1'b1, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");
		end	
		else begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, FAIL: div_val must be < 4'b1001, EXP: 1'b1, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");		        
		        cnt_fail = cnt_fail + 1;
		end
		
		read(ADDR_TCR);
	
		#1;
		if(rdata == 32'h0000_0502) begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, PASS: no enable write data, EXP: 32'h00000502, ACT: 32'h%0h", $time, rdata);
			$display("---------------------------------------------------------------------------------------");
		end	
		else begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, FAIL: no enable write data, EXP: 32'h00000502, ACT: 32'h%0h", $time, rdata);
			$display("---------------------------------------------------------------------------------------");		        
		        cnt_fail = cnt_fail + 1;
		end

	
		write(ADDR_TCR, 32'h0000_0502, 1'b0, 4'h0);
		write(ADDR_TCR, 32'h0000_0a02, 1'b0, 4'h0);

		#1;
		if(pslverr == 1'b0) begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, PASS: no error when pstrb[1] = 0, EXP: 1'b0, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");
		end	
		else begin
			$display("---------------------------------------------------------------------------------------");
			$display("t=%0d, FAIL: no error when pstrb[1] = 0, EXP: 1'b0, ACT: 1'b%0b", $time, pslverr);
			$display("---------------------------------------------------------------------------------------");		        
		        cnt_fail = cnt_fail + 1;
		end

		
		if(cnt_fail == 0) begin
			$display("==============================================");
			$display("============= PASS: SLAVE ERROR ==============");
			$display("==============================================");
		end
		else begin
			$display("==============================================");
			$display("============= FAIL: SLAVE ERROR ==============");
			$display("==============================================");
			cnt_fail = 0;
		end
	
	
	end
endtask
